CREATE VIEW vCliente
AS
SELECT
	 C.IDCLI AS IDCLI,
	 SUBSTRING(UPPER(C.NOMCLI),1,1) + SUBSTRING(LOWER(C.NOMCLI),2,50) AS NOMCLI,
	 SUBSTRING(UPPER(C.APECLI),1,1) + SUBSTRING(LOWER(C.APECLI),2,50) AS APECLI,
	 SUBSTRING(UPPER(C.DOMCLI),1,1) + SUBSTRING(LOWER(C.DOMCLI),2,50) AS DIRCLI,
	 C.DNICLI AS DNICLI,
	 C.TELCLI AS TELCLI,
	 U.DISUBI AS DISUBI,
	 ROW_NUMBER() OVER (ORDER BY C.IDCLI DESC) AS ORDEN
FROM 
     CLIENTE AS C
	 INNER JOIN UBIGEO AS U ON
	 C.CODUBI = U.CODUBI
GO

-- Vista para venta 

CREATE VIEW vVENTA
AS
SELECT
V.IDVEN AS IDVEN,
V.FECVEN AS FECVEN,
V.IDCLI AS IDCLI,
CONCAT(UPPER(C.APECLI), ', ', UPPER(C.NOMCLI)) AS NOMCLI,
V.TOTVEN
FROM VENTA AS V
INNER JOIN CLIENTE AS C
ON V.IDCLI = C.IDCLI
